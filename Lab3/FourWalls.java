/*
 * This program is written for calculating area of four walls.
 */




public class FourWalls{
    public static void main(String args[]){
        int length = 4;
        int breadth = 5;
        int height = 7;
        int area = 2* height*(length + breadth);
        System.out.println("The area of four walls is" + area);
        
    }
}