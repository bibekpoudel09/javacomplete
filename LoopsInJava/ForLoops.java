/*
 * for (initialization; condition; updation){]
 *  we initialize the value with a variable with declared datatype then we 
 *  define a termination condition and at last we define a updating condition for a loop.   
 */



public class ForLoops{
    public static void main(String[] args){
        for (int counter = 0; counter<100; counter = counter + 2){ 
            System.out.println("God is Dead.");
        }
    }
}
